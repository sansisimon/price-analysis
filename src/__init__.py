'''
¿Qué es __init__.py?
- __init__.py es un archivo especial que indica a Python que una carpeta debe ser tratada como un paquete (o subpaquete).

- Esto permite que puedas hacer importaciones del tipo from src import transformacion o import src.transformacion.

- No se crea automáticamente: hay que crearlo manualmente (puede estar vacío).

- Solo basta con que exista un archivo llamado exactamente __init__.py dentro de la carpeta src para que Python
 la considere paquete.
'''